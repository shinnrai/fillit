#include "../includes/fillit.h"

void	print(int dim)
{
	int		i;
	int		j;

	i = -1;
	while (++i < dim)
	{
		j = -1;
		while (++j < dim)
			write(1, &(g_min_map[i][j]), 1);
		write(1, "\n", 1);
	}
}
